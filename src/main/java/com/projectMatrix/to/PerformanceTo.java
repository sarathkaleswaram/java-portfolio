package com.projectMatrix.to;

public class PerformanceTo {
	private Integer performanceId;
	private String performanceLevelName;
	private Integer performanceScale;

	public Integer getPerformanceId() {
		return performanceId;
	}

	public void setPerformanceId(Integer performanceId) {
		this.performanceId = performanceId;
	}

	public String getPerformanceLevelName() {
		return performanceLevelName;
	}

	public void setPerformanceLevelName(String performanceLevelName) {
		this.performanceLevelName = performanceLevelName;
	}

	public Integer getPerformanceScale() {
		return performanceScale;
	}

	public void setPerformanceScale(Integer performanceScale) {
		this.performanceScale = performanceScale;
	}

}
