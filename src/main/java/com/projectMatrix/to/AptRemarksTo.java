import java.util.Date;

public class AptRemarksTo {

	private Long aptRemarksId;
	private String resource;
	private long actionPointTracker;
	private String remarks;
	private String createdDate;
	
	public Long getAptRemarksId() {
		return aptRemarksId;
	}
	public void setAptRemarksId(Long aptRemarksId) {
		this.aptRemarksId = aptRemarksId;
	}
	public String getResource() {
		return resource;
	}
	public void setResource(String resource) {
		this.resource = resource;
	}
	public long getActionPointTracker() {
		return actionPointTracker;
	}
	public void setActionPointTracker(long actionPointTracker) {
		this.actionPointTracker = actionPointTracker;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	
	
	
}
