package com.projectMatrix.to;

public class SkillsTo {
	private Integer skillsId;
	private String skillsName;
	private Integer skillsScale;

	public Integer getSkillsId() {
		return skillsId;
	}

	public void setSkillsId(Integer skillsId) {
		this.skillsId = skillsId;
	}

	public String getSkillsName() {
		return skillsName;
	}

	public void setSkillsName(String skillsName) {
		this.skillsName = skillsName;
	}

	public Integer getSkillsScale() {
		return skillsScale;
	}

	public void setSkillsScale(Integer skillsScale) {
		this.skillsScale = skillsScale;
	}

}
